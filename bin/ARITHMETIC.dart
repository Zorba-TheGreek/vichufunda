  print('${a + b}');//arithmetic operator
  print('${a - b}');//arithmetic operator
  print('${a / b}');//arithmetic operator
  print('${a * b}');//arithmetic operator
  print('${a ~/ b}');//arithmetic operator
  print('${a % b}');//arithmetic operator
  print('${-(a + b)}');//arithmetic operator

  print('${x=y}');//assignment operator
  print('${x+=y}');//assignment operator
  print('${x-=y}');//assignment operator
  print('${x*=y}');//assignment operator
  print('${x/=y}');//assignment operator
  print('${x~/y}');//assignment operator
  print('${(x%=y)}');//assignment operator
}



